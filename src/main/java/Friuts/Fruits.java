package Friuts;

public class Fruits {

    private double weight;
    private int amount;

    public Fruits(double weight, int amount) {
        this.weight = weight;
        this.amount = amount;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }
}


